//! `node-modules` subcommand: recursively find all `node_modules` directories
//! under `$HOME`, report their sizes and last-modified date.
//!
//! Only top-level `node_modules` dirs are reported (we don't descend into them).
//! Results are sorted largest-first.

use std::path::Path;
use std::time::SystemTime;

use walkdir::WalkDir;

use crate::model::{CliError, CliResponse, FileEntry};
use crate::scanner::{delete_path, directory_size, home_subdir};
use crate::streaming;

pub fn run(execute: bool, stream: bool) -> CliResponse {
    let operation = if execute { "node-modules-delete" } else { "node-modules-scan" };
    let mut response = CliResponse::new(operation, execute);

    let home = match home_subdir("") {
        Some(p) => p,
        None => {
            response.errors.push(CliError {
                path: "~".into(),
                message: "Could not resolve $HOME".into(),
            });
            return response.finalize();
        }
    };

    find_node_modules(&home, &mut response.files, &mut response.errors, stream);

    response.files.sort_by(|a, b| b.size_bytes.cmp(&a.size_bytes));

    if execute {
        for entry in response.files.iter_mut() {
            match delete_path(Path::new(&entry.path)) {
                Ok(()) => {
                    entry.deleted = true;
                    if stream { streaming::emit_file(entry); }
                }
                Err(err) => response.errors.push(CliError {
                    path: entry.path.clone(),
                    message: err.to_string(),
                }),
            }
        }
    }

    let finalized = response.finalize();
    if stream { streaming::emit_done(finalized.files.len(), finalized.total_bytes); }
    finalized
}

fn find_node_modules(root: &Path, files: &mut Vec<FileEntry>, errors: &mut Vec<CliError>, stream: bool) {
    let walker = WalkDir::new(root)
        .follow_links(false)
        .into_iter()
        .filter_entry(|e| {
            e.file_name() != "node_modules" || e.depth() == 0
        });

    for entry in walker {
        match entry {
            Err(err) => errors.push(CliError {
                path: err.path().map(|p| p.display().to_string())
                    .unwrap_or_else(|| root.display().to_string()),
                message: err.to_string(),
            }),
            Ok(e) if e.file_type().is_dir() && e.file_name() == "node_modules" => {
                let path = e.path();
                let mut size_errors = Vec::new();
                let size = directory_size(path, &mut size_errors);
                errors.extend(size_errors);

                let _mtime = std::fs::metadata(path)
                    .ok()
                    .and_then(|m| m.modified().ok())
                    .unwrap_or(SystemTime::UNIX_EPOCH);

                let file = FileEntry {
                    path: path.display().to_string(),
                    size_bytes: size,
                    is_dir: true,
                    deleted: false,
                };
                if stream { streaming::emit_file(&file); }
                files.push(file);
            }
            _ => {}
        }
    }
}
